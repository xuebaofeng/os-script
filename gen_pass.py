all_titles = jin_yong + gu_long + liang_yu_sheng + movies + tv_shows


def to_py(text):
    return "".join(lazy_pinyin(text)).lower()


def pick_two_chars(text):
    if len(text) <= 2:
        return text
    return "".join(random.sample(text, 2))


def generate():
    title = random.choice(all_titles)

    short = pick_two_chars(title)

    if title in jin_yong:
        t = "金庸武侠"
    elif title in gu_long:
        t = "古龙武侠"
    elif title in liang_yu_sheng:
        t = "梁羽生武侠"
    elif title in movies:
        t = "电影"
    else:
        t = "电视剧"

    return {
        "类型": t,
        "名称": title,
        "密码": to_py(short)
    }


if __name__ == "__main__":
    r = generate()

